package com.controlFlowSamples;

public class ForLoopSample {

	public static void main(String[] args) {
		int[] arrayofInts = {32,87,98,45,65,405,809,76,2000};
		
		for(int i=0; i < arrayofInts.length;i++) {
			System.out.println(arrayofInts[i]+"");
		}
        System.out.println();
	}

}
 // block of code will be run... until the code return false(For loop)